def get_fast_aji(pred, true):
    """AJI version distributed by MoNuSeg, has no permutation problem but suffered from
    over-penalisation similar to DICE2.

    Fast computation requires instance IDs are in contiguous orderding i.e [1, 2, 3, 4]
    not [2, 3, 6, 10]. Please call `remap_label` before hand and `by_size` flag has no
    effect on the result.

    """
    true = np.copy(true)  # ? do we need this
    pred = np.copy(pred)
    true_id_list = list(np.unique(true))
    pred_id_list = list(np.unique(pred))

    true_masks = [
        None,
    ]
    for t in true_id_list[1:]:
        t_mask = np.array(true == t, np.uint8)
        true_masks.append(t_mask)

    pred_masks = [
        None,
    ]
    for p in pred_id_list[1:]:
        p_mask = np.array(pred == p, np.uint8)
        pred_masks.append(p_mask)

    # prefill with value
    pairwise_inter = np.zeros(
        [len(true_id_list) - 1, len(pred_id_list) - 1], dtype=np.float64
    )
    pairwise_union = np.zeros(
        [len(true_id_list) - 1, len(pred_id_list) - 1], dtype=np.float64
    )

    # caching pairwise
    for true_id in true_id_list[1:]:  # 0-th is background
        t_mask = true_masks[true_id]
        pred_true_overlap = pred[t_mask > 0]
        pred_true_overlap_id = np.unique(pred_true_overlap)
        pred_true_overlap_id = list(pred_true_overlap_id)
        for pred_id in pred_true_overlap_id:
            if pred_id == 0:  # ignore
                continue  # overlaping background
            p_mask = pred_masks[pred_id]
            total = (t_mask + p_mask).sum()
            inter = (t_mask * p_mask).sum()
            pairwise_inter[true_id - 1, pred_id - 1] = inter
            pairwise_union[true_id - 1, pred_id - 1] = total - inter

    pairwise_iou = pairwise_inter / (pairwise_union + 1.0e-6)
    # pair of pred that give highest iou for each true, dont care
    # about reusing pred instance multiple times
    paired_pred = np.argmax(pairwise_iou, axis=1)
    pairwise_iou = np.max(pairwise_iou, axis=1)
    # exlude those dont have intersection
    paired_true = np.nonzero(pairwise_iou > 0.0)[0]
    paired_pred = paired_pred[paired_true]
    # print(paired_true.shape, paired_pred.shape)
    overall_inter = (pairwise_inter[paired_true, paired_pred]).sum()
    overall_union = (pairwise_union[paired_true, paired_pred]).sum()

    paired_true = list(paired_true + 1)  # index to instance ID
    paired_pred = list(paired_pred + 1)
    # add all unpaired GT and Prediction into the union
    unpaired_true = np.array(
        [idx for idx in true_id_list[1:] if idx not in paired_true]
    )
    unpaired_pred = np.array(
        [idx for idx in pred_id_list[1:] if idx not in paired_pred]
    )
    for true_id in unpaired_true:
        overall_union += true_masks[true_id].sum()
    for pred_id in unpaired_pred:
        overall_union += pred_masks[pred_id].sum()

    aji_score = overall_inter / overall_union
    return aji_score


class ObjDiceLoss(nn.Module):

    def __init__(self):
        super(ObjDiceLoss, self).__init__()

    def forward(self, S: torch.Tensor, G: torch.Tensor):

        def Dice(A, B):
            temp = A & B
            dice = 2 * torch.sum(temp, dtype=torch.float32) / (torch.sum(A, dtype=torch.float32) + torch.sum(B, dtype=torch.float32))
            return dice

        # 获取S中object列表及非0元素个数
        listLabelS = torch.unique(S)
        listLabelS = listLabelS[listLabelS != 0]
        numS = len(listLabelS)
        # 获取G中object列表及非0元素个数
        listLabelG = torch.unique(G)
        listLabelG = listLabelG[listLabelG != 0]
        numG = len(listLabelG)

        if numS == 0 & numG == 0:
            return 1
        elif numS == 0 | numG == 0:
            return 0

        # 记录omega_i*Dice(G_i,S_i)
        temp1 = 0.0
        # S中object总面积
        totalAreaS = torch.sum(S > 0, dtype=torch.float32)

        for iLabelS in range(numS):
            # Si为S中值为iLabelS的区域, boolean矩阵
            Si = S == listLabelS[iLabelS]
            # 找到G中对应区域并去除背景
            intersectlist = G[Si]
            intersectlist = intersectlist[intersectlist != 0]

            if len(intersectlist) != 0:
                indexGi = torch.argmax(torch.bincount(intersectlist))
                # Gi为gt中能与Si面积重合最大的object
                Gi = G == indexGi.item()
            else:
                Gi = torch.ones_like(G)
                Gi = Gi == 0

            omegai = torch.sum(Si, dtype=torch.float32) / totalAreaS
            temp1 = temp1 + omegai * Dice(Gi, Si)

        # 记录tilde_omega_i*Dice(tilde_G_i,tilde_S_i)
        temp2 = 0.0
        # G中object总面积
        totalAreaG = torch.sum(G > 0)

        for iLabelG in range(numG):
            # Si为S中值为iLabelS的区域, boolean矩阵
            tildeGi = G == listLabelG[iLabelG]
            # 找到G中对应区域并去除背景
            intersectlist = S[tildeGi]
            intersectlist = intersectlist[intersectlist != 0]

            if len(intersectlist) != 0:
                indextildeSi = torch.argmax(torch.bincount(intersectlist))
                tildeSi = S == indextildeSi.item()
            else:
                tildeSi = torch.ones_like(S)
                tildeSi = tildeSi == 0

            tildeOmegai = torch.sum(tildeGi, dtype=torch.float32) / totalAreaG
            temp2 = temp2 + tildeOmegai * Dice(tildeGi, tildeSi)

        objDice = (temp1 + temp2) / 2
        return objDice


class ObjHausLoss(nn.Module):
    def __init__(self):
        super(ObjHausLoss, self).__init__()

    def forward(self, S, G):
        def Haus(S, G):
            S = S.to(torch.int).numpy()
            G = G.to(torch.int).numpy()
            img1 = sitk.GetImageFromArray(S)
            img2 = sitk.GetImageFromArray(G)
            hausdorffcomputer = sitk.HausdorffDistanceImageFilter()
            hausdorffcomputer.Execute(img1, img2)
            return hausdorffcomputer.GetHausdorffDistance()

        tempS = S > 0
        totalAreaS = torch.sum(tempS, dtype=torch.float32)

        tempG = G > 0
        totalAreaG = torch.sum(tempG, dtype=torch.float32)

        # 获取S中object列表及非0元素个数
        listLabelS = torch.unique(S)
        listLabelS = listLabelS[listLabelS != 0]
        numS = len(listLabelS)
        # 获取G中object列表及非0元素个数
        listLabelG = torch.unique(G)
        listLabelG = listLabelG[listLabelG != 0]
        numG = len(listLabelG)

        if numS == 0:
            return float("NaN")

        # 记录omega_i*H(G_i,S_i)
        temp1 = 0.0

        for iLabelS in range(numS):
            # Si为S中值为iLabelS的区域, boolean矩阵
            Si = S == listLabelS[iLabelS]
            # 找到G中对应区域并去除背景
            intersectlist = G[Si]
            intersectlist = intersectlist[intersectlist != 0]

            if len(intersectlist) != 0:
                indexGi = torch.argmax(torch.bincount(intersectlist))
                Gi = G == indexGi.item()
            else:
                tempDist = torch.zeros(numG)

                for iLabelG in range(numG):
                    Gi = G == listLabelG[iLabelG]
                    tempDist[iLabelG] = Haus(Gi, Si)

                minIdx = torch.argmin(tempDist)
                Gi = G == listLabelG[minIdx]

            omegai = torch.sum(Si, dtype=torch.float32) / totalAreaS
            temp1 = temp1 + omegai * Haus(Gi, Si)

        # 记录tilde_omega_i*H(tilde_G_i,tilde_S_i)
        temp2 = 0.0

        for iLabelG in range(numG):
            # Si为S中值为iLabelS的区域, boolean矩阵
            tildeGi = G == listLabelG[iLabelG]
            # 找到G中对应区域并去除背景
            intersectlist = S[tildeGi]
            intersectlist = intersectlist[intersectlist != 0]

            if len(intersectlist) != 0:
                indextildeSi = torch.argmax(torch.bincount(intersectlist))
                tildeSi = S == indextildeSi.item()
            else:
                tempDist = torch.zeros(numS)

                for iLabelS in range(numS):
                    tildeSi = S == listLabelS[iLabelS]
                    tempDist[iLabelS] = Haus(tildeGi, tildeSi)

                minIdx = torch.argmin(tempDist)
                tildeSi = S == listLabelS[minIdx]

            tildeOmegai = torch.sum(tildeGi, dtype=torch.float32) / totalAreaG
            temp2 = temp2 + tildeOmegai * Haus(tildeGi, tildeSi)

        objHaus = (temp1 + temp2) / 2
        return objHaus


def assert_shape(test, reference):

    assert test.shape == reference.shape, "Shape mismatch: {} and {}".format(
        test.shape, reference.shape)


class ConfusionMatrix:

    def __init__(self, test=None, reference=None):

        self.tp = None
        self.fp = None
        self.tn = None
        self.fn = None
        self.size = None
        self.reference_empty = None
        self.reference_full = None
        self.test_empty = None
        self.test_full = None
        self.set_reference(reference)
        self.set_test(test)

    def set_test(self, test):

        self.test = test
        self.reset()

    def set_reference(self, reference):

        self.reference = reference
        self.reset()

    def reset(self):

        self.tp = None
        self.fp = None
        self.tn = None
        self.fn = None
        self.size = None
        self.test_empty = None
        self.test_full = None
        self.reference_empty = None
        self.reference_full = None

    def compute(self):

        if self.test is None or self.reference is None:
            raise ValueError("'test' and 'reference' must both be set to compute confusion matrix.")

        assert_shape(self.test, self.reference)

        self.tp = int(((self.test != 0) * (self.reference != 0)).sum())
        self.fp = int(((self.test != 0) * (self.reference == 0)).sum())
        self.tn = int(((self.test == 0) * (self.reference == 0)).sum())
        self.fn = int(((self.test == 0) * (self.reference != 0)).sum())
        self.size = int(np.prod(self.reference.shape, dtype=np.int64))
        self.test_empty = not np.any(self.test)
        self.test_full = np.all(self.test)
        self.reference_empty = not np.any(self.reference)
        self.reference_full = np.all(self.reference)

    def get_matrix(self):

        for entry in (self.tp, self.fp, self.tn, self.fn):
            if entry is None:
                self.compute()
                break

        return self.tp, self.fp, self.tn, self.fn

    def get_size(self):

        if self.size is None:
            self.compute()
        return self.size

    def get_existence(self):

        for case in (self.test_empty, self.test_full, self.reference_empty, self.reference_full):
            if case is None:
                self.compute()
                break

        return self.test_empty, self.test_full, self.reference_empty, self.reference_full


def dice(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """2TP / (2TP + FP + FN)"""

    if confusion_matrix is None:
        confusion_matrix = ConfusionMatrix(test, reference)

    tp, fp, tn, fn = confusion_matrix.get_matrix()
    test_empty, test_full, reference_empty, reference_full = confusion_matrix.get_existence()

    if test_empty and reference_empty:
        if nan_for_nonexisting:
            return float("NaN")
        else:
            return 0.

    return float(2. * tp / (2 * tp + fp + fn))


def jaccard(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """TP / (TP + FP + FN)"""

    if confusion_matrix is None:
        confusion_matrix = ConfusionMatrix(test, reference)

    tp, fp, tn, fn = confusion_matrix.get_matrix()
    test_empty, test_full, reference_empty, reference_full = confusion_matrix.get_existence()

    if test_empty and reference_empty:
        if nan_for_nonexisting:
            return float("NaN")
        else:
            return 0.

    return float(tp / (tp + fp + fn))


def precision(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """TP / (TP + FP)"""

    if confusion_matrix is None:
        confusion_matrix = ConfusionMatrix(test, reference)

    tp, fp, tn, fn = confusion_matrix.get_matrix()
    test_empty, test_full, reference_empty, reference_full = confusion_matrix.get_existence()

    if test_empty:
        if nan_for_nonexisting:
            return float("NaN")
        else:
            return 0.

    return float(tp / (tp + fp))


def sensitivity(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """TP / (TP + FN)"""

    if confusion_matrix is None:
        confusion_matrix = ConfusionMatrix(test, reference)

    tp, fp, tn, fn = confusion_matrix.get_matrix()
    test_empty, test_full, reference_empty, reference_full = confusion_matrix.get_existence()

    if reference_empty:
        if nan_for_nonexisting:
            return float("NaN")
        else:
            return 0.

    return float(tp / (tp + fn))


def recall(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """TP / (TP + FN)"""

    return sensitivity(test, reference, confusion_matrix, nan_for_nonexisting, **kwargs)


def specificity(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """TN / (TN + FP)"""

    if confusion_matrix is None:
        confusion_matrix = ConfusionMatrix(test, reference)

    tp, fp, tn, fn = confusion_matrix.get_matrix()
    test_empty, test_full, reference_empty, reference_full = confusion_matrix.get_existence()

    if reference_full:
        if nan_for_nonexisting:
            return float("NaN")
        else:
            return 0.

    return float(tn / (tn + fp))


def accuracy(test=None, reference=None, confusion_matrix=None, **kwargs):
    """(TP + TN) / (TP + FP + FN + TN)"""

    if confusion_matrix is None:
        confusion_matrix = ConfusionMatrix(test, reference)

    tp, fp, tn, fn = confusion_matrix.get_matrix()

    return float((tp + tn) / (tp + fp + tn + fn))


def fscore(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, beta=1., **kwargs):
    """(1 + b^2) * TP / ((1 + b^2) * TP + b^2 * FN + FP)"""

    precision_ = precision(test, reference, confusion_matrix, nan_for_nonexisting)
    recall_ = recall(test, reference, confusion_matrix, nan_for_nonexisting)

    return (1 + beta*beta) * precision_ * recall_ /\
        ((beta*beta * precision_) + recall_)


def false_positive_rate(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """FP / (FP + TN)"""

    return 1 - specificity(test, reference, confusion_matrix, nan_for_nonexisting)


def false_omission_rate(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """FN / (TN + FN)"""

    if confusion_matrix is None:
        confusion_matrix = ConfusionMatrix(test, reference)

    tp, fp, tn, fn = confusion_matrix.get_matrix()
    test_empty, test_full, reference_empty, reference_full = confusion_matrix.get_existence()

    if test_full:
        if nan_for_nonexisting:
            return float("NaN")
        else:
            return 0.

    return float(fn / (fn + tn))


def false_negative_rate(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """FN / (TP + FN)"""

    return 1 - sensitivity(test, reference, confusion_matrix, nan_for_nonexisting)


def true_negative_rate(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """TN / (TN + FP)"""

    return specificity(test, reference, confusion_matrix, nan_for_nonexisting)


def false_discovery_rate(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """FP / (TP + FP)"""

    return 1 - precision(test, reference, confusion_matrix, nan_for_nonexisting)


def negative_predictive_value(test=None, reference=None, confusion_matrix=None, nan_for_nonexisting=True, **kwargs):
    """TN / (TN + FN)"""

    return 1 - false_omission_rate(test, reference, confusion_matrix, nan_for_nonexisting)


